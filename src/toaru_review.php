<h2>('A`)ドクオの万有引力のようです</h2>
<p>第一レス目は一見シリアスだが、レスを重ねていく度にその世界観は少しずつ宇宙の如く広くなっていく。<br>
上手くその世界観を纏めきれるのかと言う読み手の不安をものともせずに、二人だけの世界観を作り上げていく様はかなり壮大。</p>
<p>自分の作風の個性を作中にて互いに綺麗に演出し、更に仕掛けていき、読み手を渾身のギャグで笑いに誘うなど、素晴らしい出来。<br>
恐らく二人の作者自身も、物語の終着点の見極めはかなりの難しかったであろう空気の中で、確実に風呂敷を畳もうとしている努力を感じ取れる。</p>
<p>昔懐かしのネタから流行のネタも取り入れ、混沌<<カオス>>な世界観の演出にかなり有効活用し、読者を思わず突っ込ませた。<br>
しかし最後はお決まりの展開なだけに、締めで作品の空気を今一つにしてしまった印象なのは実に惜しかった点。</p>
<p>その他にもまあ突っ込みだせば、なんだこの世界観は、作品だと正直に申してキリが無いので割愛させていただく。<br>
だが、タッグバトル記念すべき開幕を怒涛の勢いで飾った面白いカオス作品であったと言う点は揺るがない事実であるのは言うまでも無い。</p>
