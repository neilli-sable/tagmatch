ちなみに、私自身はたけのこの里派なのできのこの山（笑）派の方々の言う事はこれっぽっちも信用できない。</p>
<p>そもそも、きのこの山は持ち手の柄となる箇所が折れるともはやきのこ（笑）ですらない。何これと言わざるを得ないようなお菓子となってしまう。<br>
対してたけのこの里はそう言った事が全くなく、完璧なまでの美しいフォルム、そしてきのこ厨とは違ってカロリーを抑えたチョコの量。</p>
<p>全てにおいてきのこ（笑）を圧倒している。たけのこ圧勝である。<br>
売上においてもきのこ（笑）よりも勝っているという点で、たけのこの勝ちは揺るぎない真実であろう。</p>
